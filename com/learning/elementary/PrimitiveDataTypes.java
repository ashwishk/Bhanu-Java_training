package com.learning.elementary;

public class PrimitiveDataTypes{
    public static void main(String[] args) {
        int a = 2;        
        long c = 1000L;     
        float d = 2.4f;     
        double e = 7.5645;  

        System.out.println("a: " + a );
        System.out.println("c: " + c );
        System.out.println("d: " + d );
        System.out.println("e: " + e );
    }
}